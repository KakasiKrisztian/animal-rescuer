package org.fasttrackit;

public class RescuerFriend {
    //This is a optional class
    private String name;
    private int age;

    public RescuerFriend(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
